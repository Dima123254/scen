def bdu_technique_record(code: str) -> dict[str, str]:
    """Возвращает ``{"id", "name", "group_id", "group_name"}`` для кода СП.x.y."""
    name = BDU_TECHNIQUES.get(code, code)
    parts = code.split(".")
    if len(parts) >= 3 and parts[0] == "СП":
        group_id = f"СП.{parts[1]}"
    elif len(parts) >= 2 and parts[0] == "СП":
        group_id = f"СП.{parts[1]}"
    else:
        group_id = "СП"
    gname = BDU_GROUPS.get(group_id, "")
    return {
        "id": code,
        "name": name,
        "group_id": group_id,
        "group_name": gname,
    }


def pick_bdu_realization_methods_for_ubi(ubi: str, max_items: int = 8) -> list[dict[str, str]]:
    """Подбор способов реализации из БДУ для данного УБИ.

    Args:
        ubi: Код УБИ.
        max_items: Максимум элементов в списке.

    Returns:
        Список словарей с полями id, name, group_id, group_name.
    """
    codes = UBI_TO_BDU_TECHNIQUE_CODES.get(ubi) or _DEFAULT_BDU_CODES
    out: list[dict[str, str]] = []
    seen: set[str] = set()
    for c in codes:
        if c in seen:
            continue
        if c not in BDU_TECHNIQUES:
            continue
        seen.add(c)
        out.append(bdu_technique_record(c))
        if len(out) >= max_items:
            break
    if not out:
        for c in _DEFAULT_BDU_CODES:
            if c in BDU_TECHNIQUES:
                out.append(bdu_technique_record(c))
                if len(out) >= max_items:
                    break
    return out


def format_bdu_method_line(rec: dict[str, str]) -> str:
    """Строка для отображения в матрице / отчёте."""
    return f"{rec['id']} — {rec['name']}"


def all_bdu_techniques_list() -> list[dict[str, str]]:
    """Все записи перечня (для экспорта / отчёта)."""
    return [bdu_technique_record(k) for k in sorted(BDU_TECHNIQUES.keys(), key=_sort_sp_key)]


def _sort_sp_key(code: str) -> tuple[int, ...]:
    """Сортировка СП.1.2 < СП.1.10 < СП.2.1."""
    parts = code.replace("СП.", "").split(".")
    try:
        return tuple(int(p) for p in parts)
    except ValueError:
        return (999,)
